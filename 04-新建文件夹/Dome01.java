public class Dome01{

	public static void main(String[] args) {	

		System.out.println("hello world---01");
		//在电脑cmd中运行找到文件位置并运行;
		//先编译（javac Dome01）在执行（java Dome01）。

	//“\t”的使用。
	System.out.println("hello world---02\thello world---02\thello world---02");
	//出现多个输出对象时”\t“用于将它们隔离一定距离

	//“\n”的使用。
	System.out.println("hello world---03 \nhello world---03 \nhello world---03");
	//一句话输入多个内容时“\n”用于后一个并且换行。
         }

}